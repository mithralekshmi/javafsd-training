package com.ust.examples;
public class ExCast {

	public static void main(String[] args) {
		double num=16.625;
		System.out.println(num);
		int n=(int)num;
		System.out.println(n);
		
		int x=25;
		double b=x;
		System.out.println(b);
		float f=(float)1.3;
		System.out.println(f);
		
		byte d=(byte)f;
		
		
		int c=15;
		String dbValue=String.valueOf(c);
		System.out.println(c);
		
		
		
		
		
	}

}


