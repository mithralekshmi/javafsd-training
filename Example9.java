package com.ust.examples;
/*
 * 
 * URnary operators
 */

public class Example9 {

	public static void main(String[] args) {
		int i=10;
		System.out.println(i);
		//i++
		System.out.println(i--);
		System.out.println(i);
		//++i;
		System.out.println(--i);
		i+=1;
		System.out.println(i);
	    
		
		

	}

}
